/**
 *Protótipo da calculadora
 * Operações Básicas|: somar, subtrair, multiplicar e dividir
*/


/**
 *
 * @param a Primeiro valor da soma
 * @param b Segundo valor da soma
 * @return Retorna o resultado da soma
 */
int somar(int a, int b);

/**
 *
 * @param a primeiro valor da subtração
 * @param b segundo valor da subtração
 * @return resultado da função
 */
int subtrair(int a, int b);

/**
 *
 * @param a
 * @param b
 * @return
 */
int multiplicar(int a, int b);

/**
 *
 * @param a
 * @param b
 * @return
 */
int dividir(int a, int b);

